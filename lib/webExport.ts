const WRAPPER = (hasUserCss: boolean, userCss: string) => `import Component from './Component';
import './motion.css';${hasUserCss ? "\nimport './component.css';" : ''}

// The animation targets \`.${STAGE_CLASS} > …\` by structural position, so this
// wrapper is what the generated selectors hang off. Keep the class.
export default function MotionStage() {
  return (
    <div className="${STAGE_CLASS}">
      <Component />
    </div>
  );
}
`;

export async function buildZip(o: ExportOpts): Promise<Blob> {
  const zip = new JSZip();
  const hasHover = !!o.hover;

  zip.file('motion.css', bakeCss(o));
  zip.file('Component.jsx', o.source.trim().startsWith('<')
    // A bare JSX expression isn't a module — wrap it the same way the preview
    // does, so the file is importable.
    ? `export default function Component() {\n  return (\n${o.source.trim().replace(/;\s*$/, '')}\n  );\n}\n`
    : `${o.source.trim()}\n`);
  zip.file('MotionStage.jsx', WRAPPER(!!o.css.trim(), o.css));
  if (o.css.trim()) zip.file('component.css', `${o.css.trim()}\n`);
  if (o.hover) zip.file('motion.js', RUNTIME(sampleCurve(o.hover.curve), o.hover.duration));
  zip.file('README.md', README(o, hasHover));

  return zip.generateAsync({ type: 'blob' });
}
